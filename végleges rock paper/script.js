// app.js

// Complete logic of game inside this function
const game = () => {
	let playerScore = 0;
	let computerScore = 0;
	let moves = 0;


	// Function to
	const playGame = () => {
		const rockBtn = document.querySelector('.rock');
		const paperBtn = document.querySelector('.paper');
		const scissorBtn = document.querySelector('.scissor');
		const playerOptions = [rockBtn,paperBtn,scissorBtn];
		const computerOptions = ['üóø','üìÑ','‚úÇ']

		// Function to start playing game
		playerOptions.forEach(option => {
			option.addEventListener('click',function(){

				const movesLeft = document.querySelector('.movesleft');
				moves++;
				movesLeft.innerText = `M√©g ${3-moves} k√∂r van`;


				const choiceNumber = Math.floor(Math.random()*3);
				const computerChoice = computerOptions[choiceNumber];

				// Function to check who wins
				winner(this.innerText,computerChoice)

				// Calling gameOver function after 10 moves
				if(moves == 3){
					gameOver(playerOptions,movesLeft);
				}
			})
		})

	}

	// Function to decide winner
	const winner = (player,computer) => {
		const result = document.querySelector('.result');
		const playerScoreBoard = document.querySelector('.p-count');
		const computerScoreBoard = document.querySelector('.c-count');
		player = player.toLowerCase();
		computer = computer.toLowerCase();
		if(player === computer){
			result.textContent = 'D√∂ntetlen'
		}
		else if(player == 'üóø'){
			if(computer == 'üìÑ'){
				result.textContent = 'Sz√°m√≠t√≥g√©p nyert';
				computerScore++;
				computerScoreBoard.textContent = computerScore;

			}else{
				result.textContent = 'Nyert√©l'
				playerScore++;
				playerScoreBoard.textContent = playerScore;
			}
		}
		else if(player == '‚úÇ'){
			if(computer == 'üóø'){
				result.textContent = 'Sz√°m√≠t√≥g√©p nyert';
				computerScore++;
				computerScoreBoard.textContent = computerScore;
			}else{
				result.textContent = 'Nyert√©l';
				playerScore++;
				playerScoreBoard.textContent = playerScore;
			}
		}
		else if(player == 'üìÑ'){
			if(computer == '‚úÇ'){
				result.textContent = 'Sz√°m√≠t√≥g√©p nyert';
				computerScore++;
				computerScoreBoard.textContent = computerScore;
			}else{
				result.textContent = 'Nyert√©l';
				playerScore++;
				playerScoreBoard.textContent = playerScore;
			}
		}
	}

	// Function to run when game is over
	const gameOver = (playerOptions,movesLeft) => {

		const chooseMove = document.querySelector('.move');
		const result = document.querySelector('.result');
		const reloadBtn = document.querySelector('.reload');

		playerOptions.forEach(option => {
			option.style.display = 'none';
		})


		chooseMove.innerText = 'J√°t√©k v√©ge!!!'
		movesLeft.style.display = 'none';

		if(playerScore > computerScore){
			result.style.fontSize = '2rem';
			result.innerText = 'Nyert√©l'
			result.style.color = '#308D46';
		}
		else if(playerScore < computerScore){
			result.style.fontSize = '2rem';
			result.innerText = 'Vesztett√©l';
			result.style.color = 'red';
		}
		else{
			result.style.fontSize = '2rem';
			result.innerText = 'D√∂ntetlen';
			result.style.color = 'yellow';
		}
		reloadBtn.innerText = '√öj k√∂r';
		reloadBtn.style.display = 'flex';

		reloadBtn.addEventListener('click',() => {
			window.location.reload();
		})
	}


	// Calling playGame function inside game
	playGame();

}

// Calling the game function
game();
